function selectChapter(chapter) {
  selectedChapter = chapter;
  currentQuestion = 0;
  score = 0;

  // Use map to retain context just in case
  activeQuestions = quizData[selectedSubject][chapter].map(q => ({
    ...q,
    chapterName: chapter
  }));

  if (activeQuestions.length === 0) {
    alert("No questions found for this chapter!");
    return;
  }

  document.getElementById("chapterSection").style.display = "none";
  document.getElementById("quizSection").style.display = "block";

  loadQuestion();
}

// ---------- LOAD QUESTION ----------
function loadQuestion() {
  const q = activeQuestions[currentQuestion];

  document.getElementById("quizBox").innerHTML = `
    <div class="q-meta">
      <strong>${q.chapterName}</strong><br>
      ${q.exam} ${q.year} | ${q.session}
    </div>
    <h3>${q.question}</h3>
    
    <div class="options-grid">
      ${q.options.map((opt, i) =>
    `<label class="option-label" id="lbl-${i}">
          <input type="radio" name="option" value="${i}" onchange="handleOptionSelect(${i})">
          ${opt}
        </label>`
  ).join("")}
    </div>
  `;
}

// ---------- AUTO-ADVANCE LOGIC ----------
window.handleOptionSelect = function (selectedIndex) {
  const q = activeQuestions[currentQuestion];
  const isCorrect = (selectedIndex === q.correctOption);

  // Highlight chosen option
  const labels = document.querySelectorAll('.option-label');
  labels.forEach(lbl => {
    lbl.style.pointerEvents = 'none'; // Lock interactions
  });

  const chosenLabel = document.getElementById(`lbl-${selectedIndex}`);
  const correctLabel = document.getElementById(`lbl-${q.correctOption}`);

  if (isCorrect) {
    score++;
    chosenLabel.style.borderColor = 'var(--success-color, #10b981)';
    chosenLabel.style.backgroundColor = 'rgba(16, 185, 129, 0.1)';
  } else {
    chosenLabel.style.borderColor = 'var(--danger-color, #ef4444)';
    chosenLabel.style.backgroundColor = 'rgba(239, 68, 68, 0.1)';
    // Show correct one too
    if (correctLabel) {
      correctLabel.style.borderColor = 'var(--success-color, #10b981)';
      correctLabel.style.backgroundColor = 'rgba(16, 185, 129, 0.1)';
    }
  }

  // Auto advance after 1 second
  setTimeout(() => {
    currentQuestion++;

    if (currentQuestion < activeQuestions.length) {
      loadQuestion();
    } else {
      finishQuiz();
    }
  }, 1200);
};

// ---------- FINISH ----------
function finishQuiz() {
  // SAVE RESULT
  const result = {
    subject: selectedSubject,
    chapter: selectedChapter,
    score: score,
    total: activeQuestions.length,
    date: new Date().toISOString()
  };

  let progressData = JSON.parse(localStorage.getItem("progressData")) || [];
  progressData.push(result);
  localStorage.setItem("progressData", JSON.stringify(progressData));

  document.getElementById("quizBox").innerHTML = `
    <h2>Quiz Completed 🎉</h2>
    <p style="font-size: 1.2rem; color: var(--text-secondary); margin-top: 16px;">
      You scored <strong>${score} / ${activeQuestions.length}</strong> in ${selectedChapter}.
    </p>
    <button class="btn-outline" style="margin-top: 24px;" onclick="location.reload()">Back to Subjects</button>
  `;
}
