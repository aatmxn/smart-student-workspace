// ---------- SUBJECT ----------
function selectSubject(subject) {
  selectedSubject = subject;

  document.getElementById("subjectSection").style.display = "none";
  document.getElementById("chapterSection").style.display = "block";

  const chapterDiv = document.getElementById("chapters");
  chapterDiv.innerHTML = "";

  Object.keys(quizData[subject]).forEach(chapter => {
    const btn = document.createElement("button");
    btn.className = "subject-btn";
    btn.innerText = chapter;
    btn.onclick = () => selectChapter(chapter);
    chapterDiv.appendChild(btn);
  });
}

